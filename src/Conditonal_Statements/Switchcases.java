package Conditonal_Statements;

public class Switchcases {
    public static void main(String[] args) {
         int button = 2;

        switch (button) {
            case 1 : System.out.println("Music");
                break;
            case 2 : System.out.println("Movie");
                break;
            case 3 : System.out.println("News");
                break;
            default : System.out.println("Help");
        }
    }
}
