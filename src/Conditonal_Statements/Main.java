package Conditonal_Statements;

public class Main {
    public static void main(String[] args) {

        // Control statements in Java are used to control the flow of execution of a program based on certain conditions or loops.
        // types of control statements are if, if else, if else if, and switch case

        int a = 30;
        if (a <= 25){
            System.out.println("a is less than or equal to 25");
        }
        if (a == 29){
            System.out.println("This line will not be executed as the value of a is already 25");
        }
        else {
            System.out.println("a is greater than 25");
        }




    }
}
