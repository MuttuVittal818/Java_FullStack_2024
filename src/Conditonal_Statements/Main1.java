public class Main1 {
    public static void main(String[] args) {

        // print the largest of 2 numbers

        int a = 10;
        int b = 10;

        if (a > b) {
            // 10 >= 30
            System.out.println("The largest number a is : " + a);
        }

        if(a == b) {
            System.out.println("Both numbers are equal.");
        }

        else {
            System.out.println("The largest number b is : " + b);
        }
    }
}
